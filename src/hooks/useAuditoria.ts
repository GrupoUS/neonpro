  const getAuditLogs = async (
    filters?: AuditFilter, 
    pagination?: { page: number, limit: number }
  ): Promise<{ logs: AuditLog[], total: number }> => {
    setLoading(true)
    try {
      await new Promise(resolve => setTimeout(resolve, 500))
      
      let filteredLogs = mockAuditLogs
      
      if (filters) {
        if (filters.startDate) {
          filteredLogs = filteredLogs.filter(log => log.timestamp >= filters.startDate!)
        }
        if (filters.endDate) {
          filteredLogs = filteredLogs.filter(log => log.timestamp <= filters.endDate!)
        }
        if (filters.userId) {
          filteredLogs = filteredLogs.filter(log => log.userId === filters.userId)
        }
        if (filters.category && filters.category.length > 0) {
          filteredLogs = filteredLogs.filter(log => filters.category!.includes(log.category))
        }
        if (filters.severity && filters.severity.length > 0) {
          filteredLogs = filteredLogs.filter(log => filters.severity!.includes(log.severity))
        }
        if (filters.searchTerm) {
          const term = filters.searchTerm.toLowerCase()
          filteredLogs = filteredLogs.filter(log => 
            log.userName.toLowerCase().includes(term) ||
            log.action.toLowerCase().includes(term) ||
            log.details.toLowerCase().includes(term)
          )
        }
      }
      
      const total = filteredLogs.length
      
      if (pagination) {
        const start = (pagination.page - 1) * pagination.limit
        const end = start + pagination.limit
        filteredLogs = filteredLogs.slice(start, end)
      }
      
      return { logs: filteredLogs, total }
      
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Erro ao buscar logs de auditoria')
      return { logs: [], total: 0 }
    } finally {
      setLoading(false)
    }
  }

