# Handle script arguments
case "${1:-}" in
    "build")
        build_application
        ;;
    "edge")
        deploy_edge_functions
        ;;
    "vercel")
        deploy_main_application
        ;;
    "test")
        run_deployment_tests
        ;;
    *)
        main
        ;;
esac