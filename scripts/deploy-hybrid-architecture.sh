#!/bin/bash

# Complete Hybrid Architecture Deployment Script
# Deploys Edge Functions to Supabase and main app to Vercel
# Part of NeonPro Hybrid Architecture Migration

set -e

echo "ðŸš€ Deploying NeonPro Hybrid Architecture..."
echo "============================================"

# Check prerequisites
check_prerequisites() {
    echo "ðŸ” Checking prerequisites..."
    
    if ! command -v vercel &> /dev/null; then
        echo "âŒ Vercel CLI not found. Installing..."
        bun install -g vercel
    fi
    
    if ! command -v supabase &> /dev/null; then
        echo "âŒ Supabase CLI not found. Installing..."
        bun install -g supabase
    fi
    
    if ! command -v bun &> /dev/null; then
        echo "âŒ Bun not found. Please install Bun first."
        exit 1
    fi
    
    # Check if environment files exist
    if [ ! -f ".env" ]; then
        echo "âŒ .env file not found. Please create it first."
        exit 1
    fi
    
    echo "âœ… All prerequisites checked"
}

# Build the application
build_application() {
    echo "ðŸ—ï¸  Building application with Bun..."
    
    # Clean previous builds
    bun run clean
    
    # Install dependencies with Bun
    bun install --frozen-lockfile
    
    # Build with Turbo
    bun run build:bun
    
    echo "âœ… Application built successfully"
}

# Deploy Edge Functions to Supabase
deploy_edge_functions() {
    echo "ðŸŒ Deploying Edge Functions to Supabase..."
    
    # Link to Supabase project if not already linked
    if [ ! -f "supabase/.temp/project-ref" ]; then
        echo "ðŸ”— Linking to Supabase project..."
        supabase link --project-ref ownkoxryswokcdanrdgj
    fi
    
    # Deploy edge-reads function
    echo "ðŸ“– Deploying edge-reads function..."
    supabase functions deploy edge-reads \
        --project-ref ownkoxryswokcdanrdgj \
        --no-verify-jwt
    
    # Deploy node-writes function  
    echo "âœï¸ Deploying node-writes function..."
    supabase functions deploy node-writes \
        --project-ref ownkoxryswokcdanrdgj \
        --no-verify-jwt
    
    # Set function secrets
    echo "ðŸ”‘ Setting function secrets..."
    supabase secrets set \
        --project-ref ownkoxryswokcdanrdgj \
        SUPABASE_URL=$(grep SUPABASE_URL .env | cut -d '=' -f2) \
        SUPABASE_ANON_KEY=$(grep SUPABASE_ANON_KEY .env | cut -d '=' -f2) \
        SUPABASE_SERVICE_ROLE_KEY=$(grep SUPABASE_SERVICE_ROLE_KEY .env | cut -d '=' -f2)
    
    echo "âœ… Edge Functions deployed successfully"
}

# Deploy main application to Vercel
deploy_main_application() {
    echo "ðŸš€ Deploying main application to Vercel..."
    
    # Deploy to Vercel
    cd apps/api
    
    vercel deploy --prod \
        --yes \
        --regions gru1 \
        --build-env EDGE_RUNTIME=true
    
    cd ../..
    
    echo "âœ… Main application deployed successfully"
}

# Run post-deployment tests
run_deployment_tests() {
    echo "ðŸ§ª Running deployment tests..."
    
    # Test Edge Functions
    echo "ðŸ” Testing Edge Functions..."
    edge_health=$(curl -s https://ownkoxryswokcdanrdgj.supabase.co/functions/v1/edge-reads/health)
    echo "Edge Health: $edge_health"
    
    # Test Node Functions
    echo "ðŸ” Testing Node Functions..."
    node_health=$(curl -s https://ownkoxryswokcdanrdgj.supabase.co/functions/v1/node-writes/health)
    echo "Node Health: $node_health"
    
    echo "âœ… Deployment tests completed"
}

